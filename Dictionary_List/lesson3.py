# แน่นอน! นี่คือตัวอย่างข้อมูลที่แสดงตัวอย่างการใช้งานของแต่ละโครงสร้างข้อมูล:

# 1. **Dictionary ({})**:
#    ```python
person = {'name': 'John', 'age': 30, 'city': 'New York'}
#    ```

# 2. **List ([])**:
#    ```python
#    fruits = ['apple', 'banana', 'orange']
#    ```

# 3. **List of Dictionaries ([{}])**:
#    ```python
people = [{'name': 'John', 'age': 30}, {'name': 'Jane', 'age': 25}]
#    ```

# 4. **Tuple (())**:
#    ```python
coordinates = (10, 20)
#    ```

# 5. **Set ({})**:
#    ```python
unique_numbers = {1, 2, 3, 4, 5}
#    ```

# 6. **Nested Lists (List within a List)**:
#    ```python
matrix = [[1, 2, 3], [4, 5, 6], [7, 8, 9]]
#    ```

# 7. **Nested Dictionaries (Dictionary within a Dictionary)**:
#    ```python
employee = {'name': 'John', 'details': {'age': 30, 'position': 'Manager'}}
#    ```

# 8. **Combination of Data Structures (List ที่มี Dictionary เป็นสมาชิก)**:
#    ```python
data = [{'name': 'John', 'age': 30}, {'name': 'Jane', 'age': 25}]
#    ```

